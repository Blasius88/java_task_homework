package Task_4_1;

public class Point {
public int X;
public int Y;

    public int getX() {
        return X;
    }

    public int getY() {
        return Y;
    }

    public void setX(int x) {
        X = x;
    }

    public void setY(int y) {
        Y = y;
    }

    public Point(){}

    public Point (int x, int y)
    {
        this.X = x;
        this.Y= y;
    }
}
